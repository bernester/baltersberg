    <h1 class="bakso h3 mb-8">Das Freizeitbanden <br />Manifest</h1>

    <h2 class="bakso h4 mb-4">Pädagogik mit Courage</h2>
    <p class="copy">
      "Sei du selbst, habe Mut zum eigenen Mut" - Viel zu oft wird Kindern und
      Jugendlichen nur wenig zugetraut. Oft werden Verbots- und Komfortzonen
      geschaffen, in denen es kaum Angebote gibt Grenzen zu überwinden und sich
      Herausforderungen zu stellen.
    </p>

    <p class="copy">
      Bei der Freizeitbande geht es nicht um Dauerbespaßung und Wettkampf, nicht
      um Unterhaltungskonsum und Passivität. Stattdessen bieten wir den
      Teilnehmer*innen ein Programm das Neugier und Selbstbewusstsein fördert.
      Ynd ein Alltagskonstrukt, in dem sie sich selbst und andere besser
      kennenlernen können.
    </p>

    <h2 class="bakso h4 mb-4 mt-8">
      Interkultureller Austausch und Integration
    </h2>
    <p class="copy">
      Begegnung, Kennenlernen, Interesse aneinander und Austausch sind wichtige
      Schritte auf dem Weg zu Integration, mehr Toleranz und Akzeptanz.
    </p>
    <p>
      Wir schaffen Lösungen für eine Freizeit ohne Barrieren. Für mehr
      integration, patrizipation und interkulturellen Austausch.
    </p>

    <h2 class="bakso h4 mb-4 mt-8">Zugänglichkeit</h2>
    <p class="copy">
      Hohe Kosten und Gebühren sind für viele das größte Hindernis, um an einem
      Freizeitangebot teilzunehmen. Zwar gibt es Zuschussmöglichkeiten von
      Behörden oder öffentlichen Einrichtungen. Doch diese sind mit einem hohen
      bürokratischen Aufwand verbunden und reichen oft bei weitem nicht aus.
    </p>
    <p>
      Mit unserem unabhängigen Beitragsmodell wollen wir allen die Teilnahme
      ermöglichen. Komplett unbürokratisch und auf Vertrauensbasis.
    </p>

    <h2 class="bakso h4 mb-4 mt-8">Mitdenken! Mitbestimmen!</h2>
    <p class="copy">
      Kinder und Jugendliche sind die Expert*innen ihrer Welt. Allerdings werden
      sie selten nach ihrer Meinung gefragt und viel zu häufig von
      Entscheidungen ausgeschlossen. So werden wertvolle Chancen vergeudet und
      positive Entwicklungen blockiert. Wir bieten den Kindern zahlreiche
      Möglichkeiten und Foren aktiv mitzugestalten. In unserem Lagerparlament
      kann jede*r eigene Ideen und Wünsche einbringen, die dann diskutiert und
      gemeinsam beschlossen werden. Themen wie "Handy im Zeltlager", "Regeln"
      oder "Nachtruhe" werden nicht über die Köpfe der Kinder hinweg
      entschieden. Wir sind der Überzeugung, dass man demokratisch zu besseren
      Entscheidungen und Lösungen gelangt.
    </p>

    <h2 class="bakso h4 mb-4 mt-8">Nachhaltig, Ökologisch, Regional</h2>
    <p class="copy">
      Wir möchten den Hauptteil unserer Lebensmittel von regionalen und
      ökologischen Herstellern beziehen. Ernährungsbildung sehen wir ebenfalls
      als Teil unseres pädagogischen Auftrags.
    </p>
